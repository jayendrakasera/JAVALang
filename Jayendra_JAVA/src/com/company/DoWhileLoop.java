package com.company;

public class DoWhileLoop {
    public static void main(String[] args) {
        int a = 10;
        do {
            System.out.println(a);
            a++;
        } while (a<15);

// Question : write a programme to print first n natural numbers using do-while loops.
        int n = 0;
        do {
            System.out.println(n);
            n++;
        }while (n<=50);

    }
}
